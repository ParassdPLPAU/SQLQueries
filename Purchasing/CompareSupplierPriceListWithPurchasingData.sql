WITH RankedParts AS (
	SELECT PartNum,
	EffectiveDate,
	VendorNum,
	LeadTime,
	ROW_NUMBER() OVER (PARTITION BY PartNum ORDER BY EffectiveDate DESC) AS RowNum
	FROM erp.VendPart
)

SELECT RankedParts.PartNum AS 'PLPartNum',
EffectiveDate AS 'PLEffDate',
RankedParts.LeadTime AS 'PLLeadTime',
PartPlant.LeadTime AS 'PartLeadTime',
PartPlant.VendorNum AS 'PrimaryVendor'
FROM RankedParts 
INNER JOIN PartPlant ON 
RankedParts.PartNum = PartPlant.PartNum
AND PartPlant.VendorNum = RankedParts.VendorNum
WHERE RowNum = 1
AND plant = 'GLENDN'
AND RankedParts.LeadTime <> PartPlant.LeadTime
ORDER BY EffectiveDate desc;

